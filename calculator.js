// adds two values
function add(a, b) {
  return a + b;
}

// subtracts two values
function subtract(a, b) {
  return a - b;
}

// multiplies two values
function multiply(a, b) {
  return a * b;
}

// divides two values
function divide(a, b) {
  return a / b;
}

// takes an math operator and two values and applies the operator to the numbers
function operate(operator, a, b) {
  return operator(a, b);
}